import java.lang.*;
class BaseClass
{
BaseClass()
{
System.out.println("from superclass");
}
}

class SubClass extends BaseClass
{
SubClass()
{
super();
System.out.println("default constructor of subclass");
}
}

class SuperTest1
{
public static void main(String args[])
{
SubClass s=new SubClass();
}
}


